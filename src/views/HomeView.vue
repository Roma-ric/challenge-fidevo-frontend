import axios from 'axios';

// --------------------------------------------------- SWIPER START -------------------------------------------------
import { onBeforeUnmount, onMounted, ref } from 'vue';
import { Keyboard, Navigation } from 'swiper/modules';
import { Swiper, SwiperSlide } from 'swiper/vue';

// Import Swiper styles
import '@/css/swiper.css';
import 'swiper/css';
import 'swiper/css/navigation';

const controlledSwiper = ref(null);
const slidesPerView = ref(6);
const slides2PerView = ref(3);
const slides3PerView = ref(9);
const modules = ref([Navigation, Keyboard]);

const setControlledSwiper = (swiper: any) => {
  controlledSwiper.value = swiper;
};

const onSlideChange = () => {
  console.log('slide change');
};

const updateSlidesPerView = () => {
  if (window.innerWidth < 576) {
    slidesPerView.value = 2;
    slides2PerView.value = 2;
    slides3PerView.value = 2;
  } else if (window.innerWidth > 685 && window.innerWidth < 1000) {
    slidesPerView.value = 3;
    slides2PerView.value = 3;
    slides3PerView.value = 5;
  } else if (window.innerWidth < 768) {
    slidesPerView.value = 3;
    slides2PerView.value = 3;
    slides3PerView.value = 5;
  } else if (window.innerWidth < 812) {
    slidesPerView.value = 3;
    slides2PerView.value = 3;
    slides3PerView.value = 6;
  } else if (window.innerWidth < 992) {
    slidesPerView.value = 6;
    slides2PerView.value = 3;
    slides3PerView.value = 9;
  }
}

const listProduitFlashs = ref([] as any[])

const listProduitFoods = ref([] as any[])

const listProduitAddCard = ref([] as any[])

const listProduitBettingFun = ref([] as any[])

const listCategorieProduit = ref([] as any[])

const listProduitBackyard = ref([] as any[])

const listProduitViral = ref([] as any[])

const listProduitTopTrends = ref([] as any[])

const listProduitAAPI = ref([] as any[])

const getAllProduits = async () => {
  try {
    const response = await axios.get('http://localhost:3000/produits');
  
    if (response.data) {
      listProduitFlashs.value = response.data.filter((produit: any) => produit.categorie.nom == "Fashion");
      listProduitFoods.value = response.data.filter((produit: any) => produit.categorie.nom == "Grocery");
      listProduitAddCard.value = response.data.filter((produit: any) => produit.categorie.nom == "Home Improvement");
      listProduitBettingFun.value = response.data.filter((produit: any) => produit.categorie.nom == "Health & wellness");
      listProduitBackyard.value = response.data.filter((produit: any) => produit.categorie.nom == "Patio & Garden");
      listProduitViral.value = response.data.filter((produit: any) => produit.categorie.nom == "Fashion");
      listProduitTopTrends.value = response.data.filter((produit: any) => produit.categorie.nom == "Beauty");
      listProduitAAPI.value = response.data.filter((produit: any) => produit.categorie.nom == "Personal Care");
    }

    console.log(response);
  } catch (error) {
    console.error('Erreur lors de la recupération des produits:', error);
    throw error;
  }
}

const getAllCategories = async () => {
  try {
    const response = await axios.get('http://localhost:3000/categories');
    listCategorieProduit.value = response.data;
    console.log(response);
  } catch (error) {
    console.error('Erreur lors de la recupération des categories:', error);
    throw error;
  }
}

onMounted(() => {
  getAllProduits();
  getAllCategories();
  updateSlidesPerView();
  window.addEventListener('resize', updateSlidesPerView);
})

onBeforeUnmount(() => {
  window.removeEventListener('resize', updateSlidesPerView);
})

